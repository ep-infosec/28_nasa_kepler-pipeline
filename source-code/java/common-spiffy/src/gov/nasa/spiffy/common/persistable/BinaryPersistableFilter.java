package gov.nasa.spiffy.common.persistable;


/**
 * This class is used to specify a filter for the {@link BinaryPersistableOutputStream} class.
 * When the object tree is serialized, only the specified index range for the specified array 
 * will be included in the .bin file.
 * 
 * @author Todd Klaus todd.klaus@nasa.gov
 *
 */
public class BinaryPersistableFilter {

    private String path;
    private int startIndex;
    private int endIndex;
    
    public BinaryPersistableFilter(String path, int startIndex, int endIndex) {
        this.path = path;
        this.startIndex = startIndex;
        this.endIndex = endIndex;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public int getStartIndex() {
        return startIndex;
    }

    public void setStartIndex(int startIndex) {
        this.startIndex = startIndex;
    }

    public int getEndIndex() {
        return endIndex;
    }

    public void setEndIndex(int endIndex) {
        this.endIndex = endIndex;
    }
}
