    private static final Log log = LogFactory.getLog(DynablackRowIndexFix.class);
    
    private final static SortedSet<DynablackFitValid> spq93Run;
    
    private final static SortedSet<DynablackFitValid> vnv93Run;
    
    /**
     * 
     * @param argv
     * @throws Exception
     */
    public static void main(String[] argv) throws Exception {
        FileStoreClient fsClient = FileStoreClientFactory.getInstance();
        
        int argvi = 0;
        SortedSet<DynablackFitValid> dynablackFitInformation = null;
        if (argv[argvi].equals("spq")) {
            dynablackFitInformation = spq93Run;
        } else {
            dynablackFitInformation = vnv93Run;
        }
        
        boolean doCommit = argv[++argvi].equals("commit");
        
        DynablackFitValid startAfter = null;
        if (argv.length > 2) {
            startAfter = 
                new DynablackFitValid(Integer.parseInt(argv[++argvi]), 
                                       Integer.parseInt(argv[++argvi]),
                                       Integer.parseInt(argv[++argvi]),
                                       Integer.parseInt(argv[++argvi]),
                                       1);
        }
        
        for (DynablackFitValid valid : dynablackFitInformation) {
          if (valid.valid) {
              continue;
          }
          
          if (startAfter != null && valid.compareTo(startAfter) <= 0) {
              continue;
          }
          
          log.info("Fixing " + valid);
          fsClient.beginLocalFsTransaction();
          try {
              Map<FsId, TimeSeries> flags = fetchNext(valid, true);
              TimeSeries[] updatedFlags = update(flags, true);
              fsClient.writeTimeSeries(updatedFlags);
              //Garbage collection hint
              flags = null;
              updatedFlags = null;
              
              Map<FsId, TimeSeries> variation  = fetchNext(valid, false);
              TimeSeries[] updatedVariation = update(variation, false);
              fsClient.writeTimeSeries(updatedVariation);
              
              if (doCommit) {
                  fsClient.commitLocalFsTransaction();
              } else {
                  log.info("Transaction rolled back.");
                  fsClient.rollbackLocalFsTransaction();
              }
          } catch (Exception x) {
              log.error("While updating " + valid, x);
              try {
                  fsClient.rollbackLocalFsTransactionIfActive();
              } catch (Exception x2) {
                  log.error("Could not rollback transaction.", x2);
              }
              throw x;
          }
          
          log.info("Fix completed for " + valid);
        }
    }

    public static Map<FsId, TimeSeries> fetchNext(DynablackFitValid valid, boolean isFlag) throws Exception {
        FileStoreClient fsClient = FileStoreClientFactory.getInstance();
        
        FsId someDynablackId = null;
        if (isFlag) {
            someDynablackId = DynablackFsIdFactory.getRollingBandArtifactFlagsFsId(valid.ccdModule, valid.ccdOutput, -1, 0);
        } else {
            someDynablackId = DynablackFsIdFactory.getRollingBandArtifactVariationFsId(valid.ccdModule, valid.ccdOutput, -1, 0);
        }
        
        String query = "TimeSeries@" + someDynablackId.path() + "*";
        Set<FsId> dynablackIds = fsClient.queryIds2(query);
        
        return fsClient.readTimeSeries(dynablackIds, valid.startCadence, valid.endCadence, true);
    }
    
    public static TimeSeries[] update(Map<FsId, TimeSeries> original, boolean isFlag) {
        List<TimeSeries> updatedTimeSeries = new LinkedList<TimeSeries>();
        int i=0;
        for (TimeSeries timeSeries : original.values()) {
            ParsedRollingBandId parsedId = DynablackFsIdFactory.getRollingBandDuration(timeSeries.id());
            if (parsedId == null) {
                continue;  //There are some old flags stored without the pulse duration.
            }
            if (parsedId.row == 0) {
                continue;
            }
            if (isFlag) {
                IntTimeSeries flagTimeSeries = (IntTimeSeries) timeSeries;
                FsId newFsId = DynablackFsIdFactory
                    .getRollingBandArtifactFlagsFsId(parsedId.ccdModule, parsedId.ccdOutput, parsedId.row - 1, parsedId.duration);
                updatedTimeSeries.add(new IntTimeSeries(newFsId, flagTimeSeries.iseries(), timeSeries.startCadence(), timeSeries.endCadence(), timeSeries.validCadences(), timeSeries.originators()));
            } else {
                DoubleTimeSeries variationLevelTimeSeries = (DoubleTimeSeries) timeSeries;
                FsId newFsId = DynablackFsIdFactory
                    .getRollingBandArtifactVariationFsId(parsedId.ccdModule, parsedId.ccdOutput, parsedId.row - 1, parsedId.duration);
                updatedTimeSeries.add(new DoubleTimeSeries(newFsId, variationLevelTimeSeries.dseries(), timeSeries.startCadence(), timeSeries.endCadence(), timeSeries.validCadences(), timeSeries.originators()));
            }
            i++;
        }
        return updatedTimeSeries.toArray(new TimeSeries[0]);
    }
 
    public static final class DynablackFitValid implements Comparable<DynablackFitValid> {
        public final int startCadence;
        public final int endCadence;
        public final int ccdModule;
        public final int ccdOutput;
        public final boolean valid;
        
        public DynablackFitValid(int startCadence, int endCadence,
            int ccdModule, int ccdOutput, int valid) {
            super();
            this.startCadence = startCadence;
            this.endCadence = endCadence;
            this.ccdModule = ccdModule;
            this.ccdOutput = ccdOutput;
            this.valid = valid == 1;
        }
        @Override
        public int hashCode() {
            final int prime = 31;
            int result = 1;
            result = prime * result + ccdModule;
            result = prime * result + ccdOutput;
            result = prime * result + endCadence;
            result = prime * result + startCadence;
            return result;
        }
        @Override
        public boolean equals(Object obj) {
            if (this == obj)
                return true;
            if (obj == null)
                return false;
            if (getClass() != obj.getClass())
                return false;
            DynablackFitValid other = (DynablackFitValid) obj;
            if (ccdModule != other.ccdModule)
                return false;
            if (ccdOutput != other.ccdOutput)
                return false;
            if (endCadence != other.endCadence)
                return false;
            if (startCadence != other.startCadence)
                return false;
            return true;
        }
        @Override
        public int compareTo(DynablackFitValid o) {
            int diff = this.startCadence - o.startCadence;
            if (diff != 0) {
                return diff;
            }
            diff = this.endCadence - o.endCadence;
            if (diff != 0) {
                return diff;
            }
            diff = this.ccdModule - o.ccdModule;
            if (diff != 0) {
                return diff;
            }
            diff = this.ccdOutput - o.ccdOutput;
            if (diff != 0) {
                return diff;
            }
            return 0;
        }
        @Override
        public String toString() {
            StringBuilder builder = new StringBuilder();
            builder.append("DynablackFitValid [startCadence=")
                .append(startCadence)
                .append(", endCadence=")
                .append(endCadence)
                .append(", ccdModule=")
                .append(ccdModule)
                .append(", ccdOutput=")
                .append(ccdOutput)
                .append(", valid=")
                .append(valid)
                .append("]");
            return builder.toString();
        }
        
        
    }
    
