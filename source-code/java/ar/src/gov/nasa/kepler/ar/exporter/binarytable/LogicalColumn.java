package gov.nasa.kepler.ar.exporter.binarytable;

/**
 * Describes a FITS binary table column that stores a logical (boolean) value in
 * each column cell.
 * @author Sean McCauliff
 *
 */
public class LogicalColumn extends ColumnDescription {

    /**
     * 
     * @param type Actually the name of the column.
     * @param typeComment non-null
     * @param displayHint non-null
     * @param unit This may be null
     * @param unitComment This may be null if unit is also null.
     */
    public LogicalColumn(String type, String typeComment,
            String displayHint, String unit, String unitComment) {
        
        super(type, typeComment, 
            "L", "logical value",
            unit, unitComment,
            displayHint,
            0 /*dimensions*/, 1 /*sizeofscalar*/);
    }
    
}
