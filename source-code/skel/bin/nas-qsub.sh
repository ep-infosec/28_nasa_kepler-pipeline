# This script is used to run the PBS 'qsub' command to submit a task to Pleiades.
#
# usage: nas-tm-qsub.sh JOB_NAME WALL_TIME NUM_NODES GROUP_NAME QUEUE_NAME ARCH_TYPE WORKING_DIR KEPLER_DIST_DIR STATE_FILE_PATH
#

if [ $# -ne 9 ]
then
  echo "Usage: `basename $0` JOB_NAME WALL_TIME NUM_NODES GROUP_NAME QUEUE_NAME ARCH_TYPE WORKING_DIR KEPLER_DIST_DIR STATE_FILE_PATH"
  exit -1
fi

JOB_NAME=${1}
WALL_TIME=${2}
NUM_NODES=${3}
GROUP_NAME=${4}
QUEUE_NAME=${5}
ARCH_TYPE=${6}
WORKING_DIR=${7}
KEPLER_DIST_DIR=${8}
STATE_FILE_PATH=${9}

KEPLER_BIN_DIR="$KEPLER_DIST_DIR/bin"

SCRIPT_ARGS="$WORKING_DIR $KEPLER_DIST_DIR $STATE_FILE_PATH"
QSUB_ARGS="-N $JOB_NAME -q $QUEUE_NAME -rn -l walltime=$WALL_TIME -l select=$NUM_NODES:model=$ARCH_TYPE -W group_list=$GROUP_NAME"

echo "SCRIPT_ARGS=$SCRIPT_ARGS"
echo "QSUB_ARGS=$QSUB_ARGS"

echo ${KEPLER_BIN_DIR}/nas-task-master.sh ${SCRIPT_ARGS} | qsub ${QSUB_ARGS}





