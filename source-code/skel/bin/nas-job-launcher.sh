# This script is used to execute a single job on Pleiades

# No login scripts (.bash_profile, .bashrc, etc.) are run prior to running
# the script, so the script must initialize all necessary environment variables.
#
# usage: nas-job-launcher.sh TASK_DIR SUBTASK_DIR GROUP_INDEX SUBTASK_INDEX BINARY_NAME TIMEOUT_SECS KEPLER_DIST_DIR
#

if [ $# -ne 7 ]
then
  echo "Usage: `basename $0` TASK_DIR SUBTASK_DIR GROUP_INDEX SUBTASK_INDEX BINARY_NAME TIMEOUT_SECS KEPLER_DIST_DIR"
  exit -1
fi

TASK_DIR=$1
SUBTASK_DIR=$2
GROUP_INDEX=$3
SUBTASK_INDEX=$4
BINARY_NAME=$5
TIMEOUT_SECS=$6
KEPLER_DIST_DIR=$7

# initialize 'module' command
. /usr/share/modules/init/bash

# load needed modules
module add jvm/jdk1.7.0_141
module add matlab/2010b

# Startup performance of compiled code can be poor if using a network filesystem like /home or NFS
# for the MCR cache directory.
# The variable MCR_CACHE_ROOT lets the cache relocate to the local node.

#define the location, unique to job
export MCR_CACHE_ROOT=/tmp/mcr_cache_`basename $TASK_DIR`
#create the cache directory
mkdir -p $MCR_CACHE_ROOT 

export SOC_CODE_ROOT=$KEPLER_DIST_DIR/..

LOG_FILE_NAME=`basename $TASK_DIR`-st-$SUBTASK_INDEX
LOG4J_LOGFILE_PREFIX=$SUBTASK_DIR/$LOG_FILE_NAME

$KEPLER_DIST_DIR/bin/runjava -Dlog4j.logfile.prefix=$LOG4J_LOGFILE_PREFIX launch-matlab $TASK_DIR $GROUP_INDEX $SUBTASK_INDEX $BINARY_NAME $TIMEOUT_SECS

